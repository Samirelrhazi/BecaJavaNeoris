package ejerciciosJava4;

public class Comprobar {

	public static void main(String[] args) {
		int valor = 0;
		valor=23;
		valor +=10;
		System.out.println("El resultado a=\t"+ --valor);
		System.out.println("El resultado b=\t"+ ++valor);
		System.out.println("El resultado c=\t"+ valor++);
		System.out.println("El resultado d=\t"+ valor--);
		System.out.println("El resultado e=\t"+ valor);
		System.out.println("\nPredeccion: \na = 32\nb = 33\nc = 33\nd = 34\ne = 33\n");


	}

}
